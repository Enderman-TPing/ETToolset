package com.github.ettoolset.tools.deamon;

/**
 * RunMethod declares the method that you may use in a deamon process <br/>
 * The methods contains RunMethod.GUI and RunMethod.CONSOLE<br/>
 * For RunMethod.GUI, the output of deamon info will be shown in a window<br/>
 * For RunMethod.CONSOLE, the output will be printed as log (in this project)
 * @author Enderman-Teleporting
 */
public enum RunMethod {
    GUI,CONSOLE
}
